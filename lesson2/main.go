package main

import "fmt"	// import statement is used to include other packages

func main() {	// main function is the entry point of the program
	fmt.Println("Hello, World!")	// Print "Hello, World!" to the console
	fmt.Println("Welcome to Lesson 2") // Print "Welcome to Lesson 2" to the console
	fmt.Println(10 + 20)          // Print the result of 10 + 20
	fmt.Println(true && false)   // Print the result of true AND false
	fmt.Println("Lesson 2 is fun!") // Print "Lesson 2 is fun!" to the console
	fmt.Println(5 * 6)           // Print the result of 5 * 6
	fmt.Println(false || true)   // Print the result of false OR true
}